    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int cf = JOptionPane.showConfirmDialog(null, "Bạn có chắc xóa bệnh nhân này không ?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (cf == JOptionPane.YES_OPTION)
        {
            String hoTen = tblBenhNhan.getValueAt(tblBenhNhan.getSelectedRow(), 1).toString();
            if (this.bnd.delete(bnd.find(hoTen)))
            {
                JOptionPane.showMessageDialog(null, "Xóa bệnh nhân thành công");
                LoadData();Main.date = new Date();
                    Main.addLog(Main.dateFormat.format(Main.date) +" Xóa bệnh nhân thành công " +"[Tên =" + hoTen + "]");
            }
            else
            {
                JOptionPane.showMessageDialog(null, " Xóa bệnh nhân thất bại");
                Main.date = new Date();
                Main.addLog(Main.dateFormat.format(Main.date) +" Xóa bệnh nhân thất bại " +"[Tên =" + hoTen + "]");
            }
        }
        else
        {
            Main.addLog("Xóa bệnh nhân thất bại");
         
        }
    }//GEN-LAST:event_jButton2ActionPerformed

