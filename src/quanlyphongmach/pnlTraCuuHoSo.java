    static void LoadData(){
        DefaultTableModel tblModel = new DefaultTableModel();
        tblModel.addColumn("STT");
        tblModel.addColumn("Họ tên");
        tblModel.addColumn("Giới tính");
        tblModel.addColumn("Năm sinh");
        tblModel.addColumn("Địa chỉ");
        int i = 0;
        for (Dsbenhnhan bn: bnd.findAll()){
            i++;
            tblModel.addRow(new Object[]{i, bn.getHoTen(), bn.getGioiTinh(), bn.getNamSinh(), bn.getDiaChi()});
                    
       }
        tblBenhNhan.setModel(tblModel);
        tblBenhNhan.repaint();
        tblBenhNhan.revalidate();

    }

